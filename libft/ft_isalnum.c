 int	_isdigit(int c)
{
	if (c >= '0' && c <= '9')
		return (2048);
	return (0);
}

 int	_isalpha(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1024);
	return (0);
}

int ft_isalnum(int c)
{
	if (_isdigit(c) || _isdigit(c))
		return (8);
	return (0);
}